export function useI18n() {
  const settings = useSettingsStore()

  function t(key, params) {
    const val = messages[settings.locale]?.[key] ?? messages.zh[key] ?? key
    if (params && typeof val === 'string') {
      return val.replace(/\{(\w+)\}/g, (_, k) => params[k] ?? `{${k}}`)
    }
    return val
  }

  const locale = computed(() => settings.locale)
  const serverLabel = computed(() => t(`server.${settings.server}`))

  return { t, locale, serverLabel }
}
